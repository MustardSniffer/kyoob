using System;

namespace Kyoob.Graphics
{
    /// <summary>
    /// An enumeration of possible render modes.
    /// </summary>
    public enum RenderMode
    {
        /// <summary>
        /// Normal render mode.
        /// </summary>
        Normal,

        /// <summary>
        /// Renders the light map only.
        /// </summary>
        LightMap,

        /// <summary>
        /// Renders the normal map only.
        /// </summary>
        NormalMap
    }
}